# 1. 테스트 케이스 개수 T를 입력받습니다.
T = int(input())

# 2. 각 테스트 케이스(1부터 T까지)에 대해 반복합니다.
for test_case in range(1, T + 1):
    
    # 3. N (총 인원), A (P채널 구독자), B (T채널 구독자)를
    #    공백으로 구분하여 정수로 입력받습니다.
    N, A, B = map(int, input().split())
    
    # 4. 'P와 T를 모두 구독'하는 사람의 [최댓값]을 계산합니다.
    #    최댓값은 P와 T 구독자 수 중 더 적은 쪽(min(A, B))입니다.
    #    (예: A=3, B=5이면 최대 3명만 둘 다 구독 가능)
    max_both = min(A, B)
    
    # 5. 'P와 T를 모두 구독'하는 사람의 [최솟값]을 계산합니다.
    #    최솟값은 (A + B - N)입니다. (전체 인원을 초과하는 만큼)
    #    만약 (A + B)가 N을 초과하지 않으면, 최솟값은 0입니다.
    min_both = max(0, A + B - N)
        
    # 6. 형식에 맞춰 테스트 케이스 번호, 최댓값, 최솟값을 출력합니다.
    print(f"#{test_case} {max_both} {min_both}")