# 1. 테스트 케이스 개수 T를 입력받습니다.
T = int(input())

for _ in range(T):
    
    # 2. 문자열을 입력받습니다.
    s = input()
    
    # 3. 5개의 줄을 빈 문자열로 초기화합니다.
    rows = ["", "", "", "", ""]
    
    # 4. 문자열의 각 문자(char)에 대해 "반복 부분"(4칸)을 누적합니다.
    for char in s:
        rows[0] += "..#."
        rows[1] += ".#.#"
        rows[2] += "#." + char + "."
        rows[3] += ".#.#"
        rows[4] += "..#."
        
    # 5. 모든 반복이 끝난 후, 각 줄에 "마무리"(1칸)를 추가합니다.
    rows[0] += "."
    rows[1] += "."
    rows[2] += "#"
    rows[3] += "."
    rows[4] += "."
    
    # 6. 5줄을 순서대로 출력합니다.
    for line in rows:
        print(line)