# 1. 테스트 케이스 개수 T를 입력받습니다.
T = int(input())

# 2. 각 테스트 케이스(1부터 T까지)에 대해 반복합니다.
for test_case in range(1, T + 1):
    
    # 3. 상자의 개수 N과 작업 횟수 Q를 공백으로 구분하여 입력받습니다.
    N, Q = map(int, input().split())
    
    # 4. N개의 상자를 0으로 초기화하는 리스트 'boxes'를 생성합니다.
    boxes = [0] * N
    
    # 5. 1번째 작업부터 Q번째 작업까지 순서대로 반복합니다.
    #    (이때 i는 작업 번호 1, 2, ... Q가 됩니다)
    for i in range(1, Q + 1):
        
        # 6. i번째 작업에 사용할 L(시작 상자)과 R(끝 상자)을 입력받습니다.
        L, R = map(int, input().split())
        
        # 7. L번 상자부터 R번 상자까지 순회합니다.
        #    (리스트 인덱스는 0부터 시작하므로 L-1부터 R-1까지입니다)
        for j in range(L - 1, R):
            
            # 8. j번 인덱스의 상자 값을 현재 작업 번호 'i'로 덮어씁니다.
            boxes[j] = i
            
    # 9. 모든 작업을 마친 후, 형식에 맞춰 테스트 케이스 번호와
    #    최종 상자 상태('boxes' 리스트)를 출력합니다.
    print(f"#{test_case}", *boxes)