# 1. 테스트 케이스 개수 T를 입력받습니다.
T = int(input())

# 2. 각 테스트 케이스(1부터 T까지)에 대해 반복합니다.
for test_case in range(1, T + 1):
    
    # 3. 0보다 큰 10진수 N을 실수(float)로 입력받습니다.
    #    (입력 예: "0.625")
    N = float(input())
    
    # 4. 이진수 변환 결과를 저장할 리스트(binary_digits)를 초기화합니다.
    binary_digits = []
    
    # 5. 13자리 이상이 필요한지 확인하기 위해, 12자리까지만 반복합니다.
    for _ in range(12):
        
        # 6. N에 2를 곱합니다. (예: 0.625 -> 1.25)
        N *= 2
        
        # 7-1. N이 1.0보다 크거나 같으면, 정수부가 '1'입니다.
        if N >= 1.0:
            # 8. 결과 리스트에 '1'을 추가합니다.
            binary_digits.append('1')
            
            # 9. N에서 1.0을 빼서 다시 0.x 형태의 소수부로 만듭니다. (예: 1.25 -> 0.25)
            N -= 1.0
            
        # 7-2. N이 1.0보다 작으면, 정수부가 '0'입니다.
        else:
            # 10. 결과 리스트에 '0'을 추가합니다. (예: 0.25 -> 0.5)
            binary_digits.append('0')
            
        # 11. 만약 N이 0.0이 되었다면 (딱 나누어 떨어짐), 
        #     더 이상 계산할 필요가 없으므로 반복을 중단합니다.
        if N == 0.0:
            break
            
    # 12. 12번의 반복이 끝난 후, 결과를 판별합니다.
    
    # 13-1. 만약 N이 0.0이 되었다면 (12자리 이내에 변환 완료),
    #       리스트를 문자열로 합쳐 결과를 만듭니다. (예: ['1','0','1'] -> "101")
    if N == 0.0:
        result = "".join(binary_digits)
        
    # 13-2. 만약 N이 0.0이 아니라면 (12번을 반복해도 소수부가 남음),
    #       13자리 이상이 필요한 경우이므로 "overflow"입니다.
    else:
        result = "overflow"
            
    # 14. 형식에 맞춰 테스트 케이스 번호와 결과를 출력합니다.
    print(f"#{test_case} {result}")